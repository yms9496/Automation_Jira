package apiEngine.model.response;

public class Token {
	
	public String token;
	 public String expires;
	 public String status;
	 public String result;
	 
	public String getToken() {
		return token;
	}
	public String getExpires() {
		return expires;
	}
	public String getStatus() {
		return status;
	}
	public String getResult() {
		return result;
	}

}
